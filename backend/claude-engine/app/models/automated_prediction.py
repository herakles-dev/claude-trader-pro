"""
SQLAlchemy Model for Automated Predictions

This module defines the database model for hourly automated predictions.

Author: Claude Code
Date: 2025-11-12
"""

from datetime import datetime
from decimal import Decimal
from typing import Optional
from uuid import uuid4

from sqlalchemy import (
    Column, Integer, String, TIMESTAMP, Numeric, Text, Boolean,
    CheckConstraint, Index, func, ForeignKey
)
from sqlalchemy.dialects.postgresql import UUID, JSONB
from sqlalchemy.orm import relationship

from app.models.prediction import Base


class AutomatedPrediction(Base):
    """
    Automated hourly predictions generated by the scheduler.
    
    Each prediction is part of a 4-hour cycle and includes full market context,
    reasoning, and performance tracking.
    """
    __tablename__ = 'automated_predictions'
    __table_args__ = (
        CheckConstraint(
            "prediction_type IN ('up', 'down')",
            name='automated_predictions_prediction_type_check'
        ),
        CheckConstraint(
            "confidence >= 0 AND confidence <= 1",
            name='automated_predictions_confidence_check'
        ),
        CheckConstraint(
            "strategy IN ('conservative', 'aggressive')",
            name='automated_predictions_strategy_check'
        ),
        CheckConstraint(
            "indicator_alignment IN ('aligned', 'conflicting', 'mixed')",
            name='automated_predictions_indicator_alignment_check'
        ),
        CheckConstraint(
            "cycle_hour >= 1 AND cycle_hour <= 4",
            name='automated_predictions_cycle_hour_check'
        ),
        CheckConstraint(
            "actual_outcome IN ('up', 'down', 'neutral', NULL)",
            name='automated_predictions_actual_outcome_check'
        ),
        Index('idx_automated_predictions_cycle_id', 'cycle_id'),
        Index('idx_automated_predictions_symbol', 'symbol'),
        Index('idx_automated_predictions_created_at', 'created_at'),
        Index('idx_automated_predictions_was_correct', 'was_correct'),
        {'schema': 'trading_predictions'}
    )
    
    # Primary Key
    id = Column(
        UUID(as_uuid=True),
        primary_key=True,
        default=uuid4,
        comment='Unique prediction identifier'
    )
    
    # Prediction Details
    symbol = Column(
        String(20),
        nullable=False,
        default='BTC/USDT',
        comment='Trading pair'
    )
    prediction_type = Column(
        String(10),
        nullable=False,
        comment="Prediction direction: 'up' or 'down'"
    )
    confidence = Column(
        Numeric(5, 4),
        nullable=False,
        comment='Confidence score (0-1)'
    )
    reasoning = Column(
        Text,
        nullable=False,
        comment='AI-generated reasoning'
    )
    
    # Model Information
    claude_model = Column(
        String(50),
        nullable=False,
        default='claude-sonnet-4-20250514',
        comment='Claude model version used'
    )
    prompt_version = Column(
        String(20),
        nullable=False,
        default='2.0.0',
        comment='Prompt template version'
    )
    strategy = Column(
        String(20),
        nullable=False,
        default='conservative',
        comment='Trading strategy used'
    )
    
    # Market Context
    market_context = Column(
        JSONB,
        nullable=False,
        comment='Full market data snapshot (JSONB)'
    )
    trend_analysis = Column(
        Text,
        nullable=True,
        comment='Trend analysis text'
    )
    indicator_alignment = Column(
        String(20),
        nullable=True,
        comment="Indicator alignment: 'aligned', 'conflicting', 'mixed'"
    )
    
    # Cost and Performance Tracking
    input_tokens = Column(
        Integer,
        nullable=False,
        comment='Input tokens used'
    )
    output_tokens = Column(
        Integer,
        nullable=False,
        comment='Output tokens generated'
    )
    cached_tokens = Column(
        Integer,
        default=0,
        comment='Cached tokens used'
    )
    total_cost_usd = Column(
        Numeric(10, 8),
        nullable=False,
        comment='Total API cost in USD'
    )
    api_latency_ms = Column(
        Integer,
        nullable=False,
        comment='API latency in milliseconds'
    )
    
    # Cycle Tracking
    cycle_id = Column(
        UUID(as_uuid=True),
        ForeignKey('trading_predictions.prediction_cycles.id', ondelete='CASCADE'),
        nullable=True,
        comment='Parent prediction cycle ID'
    )
    cycle_hour = Column(
        Integer,
        nullable=False,
        comment='Hour within 4-hour cycle (1-4)'
    )
    
    # Outcome Tracking (filled after evaluation)
    actual_outcome = Column(
        String(10),
        nullable=True,
        comment="Actual market movement: 'up', 'down', 'neutral'"
    )
    actual_price_change = Column(
        Numeric(10, 4),
        nullable=True,
        comment='Actual price change percentage'
    )
    was_correct = Column(
        Boolean,
        nullable=True,
        comment='Whether prediction was correct'
    )
    evaluated_at = Column(
        TIMESTAMP(timezone=True),
        nullable=True,
        comment='When prediction was evaluated'
    )
    
    # Timestamps
    created_at = Column(
        TIMESTAMP(timezone=True),
        default=func.now(),
        comment='Record creation time'
    )
    
    def __repr__(self):
        return (
            f"<AutomatedPrediction(id={self.id}, symbol='{self.symbol}', "
            f"prediction='{self.prediction_type}', confidence={self.confidence}, "
            f"cycle_hour={self.cycle_hour})>"
        )
    
    def to_dict(self):
        """Convert model to dictionary for JSON serialization"""
        return {
            'id': str(self.id),
            'symbol': self.symbol,
            'prediction_type': self.prediction_type,
            'confidence': float(self.confidence),
            'reasoning': self.reasoning,
            'claude_model': self.claude_model,
            'prompt_version': self.prompt_version,
            'strategy': self.strategy,
            'market_context': self.market_context,
            'trend_analysis': self.trend_analysis,
            'indicator_alignment': self.indicator_alignment,
            'input_tokens': self.input_tokens,
            'output_tokens': self.output_tokens,
            'cached_tokens': self.cached_tokens,
            'total_cost_usd': float(self.total_cost_usd) if self.total_cost_usd else None,
            'api_latency_ms': self.api_latency_ms,
            'cycle_id': str(self.cycle_id) if self.cycle_id else None,
            'cycle_hour': self.cycle_hour,
            'actual_outcome': self.actual_outcome,
            'actual_price_change': float(self.actual_price_change) if self.actual_price_change else None,
            'was_correct': self.was_correct,
            'evaluated_at': self.evaluated_at.isoformat() if self.evaluated_at else None,
            'created_at': self.created_at.isoformat() if self.created_at else None
        }
